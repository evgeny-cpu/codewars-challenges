public class Main {
    public static void main(String[] args) {

        System.out.println(quadratic(7,4.00e+13,8));
    }

    public static double quadratic(double a, double b, double c) {
        double sqr = Math.sqrt(b * b - 4 * a  * c);
        return (2 *c ) / (-b - sqr);
    }
}